public class Project34 {
    public static void main(String[] args) {

        for (int i=1; i<5; i++){
            for (int j=1; j<=i; j++){

                System.out.print("* ");
                }


            System.out.println("\n");
            }
        for (int k=3; k>=1; k--){
            for (int l=k; l>=1; l--){
                System.out.print("* ");
            }
            System.out.println("\n");

        }
        }
    }

