public class Array_pj5 {
    public static void main(String[] args) {

        int[][] num={{2,4,5,8},{6,8,8,6},{1,5,8,1},{2,3,5,4}} ;

        for (int i=0;i< num.length; i++){

            for (int j=0;j< num.length; j++){
                System.out.print(num[i][j]+" ");
            }
            System.out.println();

        }
    }
}
